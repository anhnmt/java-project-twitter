    private void txtSigninUsernameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtSigninUsernameFocusLost
        if (txtSigninUsername.getText().trim().equals("")) {
            txtSigninUsername.setText("Nhập tài khoản của bạn");
            txtSigninUsername.setForeground(new Color(153, 153, 153));
        } else {
            txtSigninUsername.setForeground(new Color(0, 0, 0));
        }
    }//GEN-LAST:event_txtSigninUsernameFocusLost

